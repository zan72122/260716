// ================================================================
// 楽曲データ — ステップシーケンサ形式(1小節=16分音符×16)
//   drums: 'x'=普通 'X'=アクセント '.'=休み
//   bass/lead/bell: [ステップ, 音名, 長さ(16分)]
//   chords: [ステップ, [音名...], 長さ(16分)]
// どの曲も 4/4。ゲーム曲は 2小節イントロ + 16小節本編 = 18小節。
// ================================================================

/** 曲の総小節数 */
export function songMeasures(spec) {
  let m = 0;
  for (const s of spec.sections) m += (s.repeat || 1);
  return m;
}

/** 曲の総拍数 */
export function songBeats(spec) {
  return songMeasures(spec) * 4;
}

